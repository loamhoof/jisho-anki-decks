package main

import (
	"encoding/csv"
	"encoding/json"
	"flag"
	"fmt"
	"log"
	"os"
	"regexp"
	"strings"
)

var cmd string

func init() {
	flag.StringVar(&cmd, "cmd", "", "Command (word/collocation)")

	flag.Parse()
}

func main() {
	f, err := os.Open("words")
	if err != nil {
		log.Fatalln(err)
	}
	defer f.Close()

	words := make(map[string]*Word)
	if err := json.NewDecoder(f).Decode(&words); err != nil {
		log.Fatalln(err)
	}

	records := make([]*Record, 0, 1024)

	switch cmd {
	case "word":
		for key, word := range words {
			if _, ok := dict[key]; ok {
				continue
			}

			records = append(records, buildRecord(word))
		}
	case "collocation":
		for _, word := range words {
			for _, collocation := range word.Collocations {
				records = append(records, buildRecord(collocation.Word))
			}
		}
	default:
		log.Fatalln("No command specified")
	}

	w := csv.NewWriter(os.Stdout)

	for _, record := range records {
		w.Write(record.MarshalCSV())
	}

	w.Flush()
}

func buildRecord(word *Word) *Record {
	rec := &Record{
		Reading:       word.Japanese[0].Reading,
		PartsOfSpeech: buildPartsOfSpeech(word.Senses[0].PartsOfSpeech),
		Meaning:       buildMeaning(word),
	}

	if word.Japanese[0].Word == "" {
		rec.Kanji = word.Japanese[0].Reading
		rec.Furigana = word.Japanese[0].Reading
	} else {
		rec.Kanji = word.Japanese[0].Word
		rec.Furigana = buildFurigana(rec.Kanji, rec.Reading)
	}

	if word.Collocations != nil {
		collocations := make([]string, len(word.Collocations))
		for i, collocation := range word.Collocations {
			collocations[i] = collocation.Raw
		}

		rec.Collocations = strings.Join(collocations, "<br />")
	}

	if word.Audios != nil {
		rec.Audio = fmt.Sprintf("[sound:%s]", word.Audios["audio/mpeg"].Filename)
	}

	if word.IsCommon {
		rec.IsCommon = "x"
	}

	rec.Link = fmt.Sprintf("http://jisho.org/word/%s", word.JishoWordPage)

	return rec
}

func buildPartsOfSpeech(parts []string) string {
	return strings.Join(parts, "; ")
}

func buildMeaning(word *Word) string {
	meanings := make([]string, len(word.Senses))

	for i, sense := range word.Senses {
		definitions := strings.Join(sense.EnglishDefinitions, "; ")
		tags := strings.Join(sense.Tags, "; ")

		meanings[i] = fmt.Sprintf("<span class=\"small grey\">%v.</span> %s", i+1, definitions)
		if tags != "" {
			meanings[i] += fmt.Sprintf("<span class=\"small grey\"> (%s)</span>", tags)
		}
	}

	return strings.Join(meanings, "<br />")
}

var findKanjiReg = regexp.MustCompile("[\u4e00-\u9faf]+")

func buildFurigana(kanji, reading string) string {
	kanjiMatches := findKanjiReg.FindAllStringIndex(kanji, -1)

	findFuriganaReg := regexp.MustCompile("^" + findKanjiReg.ReplaceAllString(kanji, "(.+)") + "$")
	furiganaMatches := findFuriganaReg.FindStringSubmatch(reading)

	furigana := kanji
	for i := len(furiganaMatches) - 1; i > 0; i-- {
		kanjiMatch := kanjiMatches[i-1]

		furigana = fmt.Sprintf(
			"%s %s[%s]%s",
			furigana[:kanjiMatch[0]],
			kanji[kanjiMatch[0]:kanjiMatch[1]],
			furiganaMatches[i],
			furigana[kanjiMatch[1]:])
	}

	return furigana
}

type Record struct {
	Kanji         string
	Furigana      string
	Reading       string
	PartsOfSpeech string
	Meaning       string
	Collocations  string
	Audio         string
	IsCommon      string
	Link          string
}

func (rec *Record) MarshalCSV() []string {
	return []string{
		rec.Kanji,
		rec.Furigana,
		rec.Reading,
		rec.PartsOfSpeech,
		rec.Meaning,
		rec.Collocations,
		rec.Audio,
		rec.IsCommon,
		rec.Link,
	}
}

type Word struct {
	IsCommon bool     `json:"is_common"`
	Tags     []string `json:"tags"`
	Japanese []*struct {
		Word    string `json:"word"`
		Reading string `json:"reading"`
	} `json:"japanese"`
	Senses []*struct {
		EnglishDefinitions []string `json:"english_definitions"`
		PartsOfSpeech      []string `json:"parts_of_speech"`
		Links              []*struct {
			Text string `json:"text"`
			Url  string `json:"url"`
		} `json:"links"`
		Tags         []string `json:"tags"`
		Restrictions []string `json:"restrictions"`
		SeeAlso      []string `json:"see_also"`
		Antonyms     []string `json:"antonyms"`
		Source       []*struct {
			Language string `json:"language"`
			Word     string `json:"word"`
		} `json:"source"`
		Info []*string `json:"info"`
	} `json:"senses"`
	Attribution struct {
		Jmdict   bool        `json:"jmdict"`
		Jmnedict bool        `json:"jmnedict"`
		Dbpedia  interface{} `json:"dbpedia"`
	} `json:"attribution"`
	Collocations  Collocations `json:"collocations,omitempty"`
	Audios        Audios       `json:"audios,omitempty"`
	JishoWordPage string       `json:"jisho_word_page"`
}

type Collocations []*Collocation

type Collocation struct {
	Raw  string `json:"raw"`
	Src  string `json:"src"`
	Word *Word  `json:"word"`
}

type Audios map[string]*Audio

type Audio struct {
	Src      string `json:"src"`
	Filename string `json:"filename"`
}

